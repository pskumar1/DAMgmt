--Create Schemas and Tables
DROP SCHEMA IF EXISTS HR CASCADE ;
DROP SCHEMA IF EXISTS FINANCE CASCADE;

CREATE SCHEMA HR;
CREATE SCHEMA FINANCE;

DROP TABLE IF EXISTS HR.PERSON;
DROP TABLE IF EXISTS HR.TYPE;
DROP TABLE IF EXISTS HR.ADDRESS;
DROP TABLE IF EXISTS FINANCE.COSTCENTER;
DROP TABLE IF EXISTS HR.PERSON_COSTCENTER;

CREATE TABLE HR.TYPE(
   TYPE_ID              INT             PRIMARY KEY,
   TYPE_NAME            VARCHAR(30)     NOT NULL,
   TYPE_DESC            VARCHAR(30)     NULL,
   PARENT_TYP_ID        INT             NOT NULL   
);
COMMENT ON TABLE HR.TYPE IS 'Table that contains static data/starter data set of allowable values';

CREATE TABLE HR.PERSON (
   PERSON_ID      SERIAL            PRIMARY KEY ,
   EMPLID         VARCHAR(10)    UNIQUE NOT NULL,
   USER_ID        VARCHAR(10)    UNIQUE NOT NULL,
   FIRST_NAME     VARCHAR(30)    NOT NULL,
   MIDDLE_NAME    VARCHAR(30)    NULL,
   LAST_NAME      VARCHAR(30)    NOT NULL,
   PREF_NAME      VARCHAR(60)    NULL,
   STATUS_TYP     INT            NOT NULL REFERENCES HR.TYPE(TYPE_ID),
   HIRE_DATE      DATE           NOT NULL,
   JOB_TITLE	  VARCHAR(60)	 NOT NULL,
   MANAGER_EMPLID VARCHAR(10)    NOT NULL   
);
COMMENT ON TABLE HR.PERSON IS 'Table that contains a Person basic data elements';

CREATE TABLE HR.ADDRESS (
   ADDRESS_ID   SERIAL             PRIMARY KEY,
   PERSON_ID    INT             NOT NULL REFERENCES HR.PERSON(PERSON_ID),
   ADDR_TYP     INT             NOT NULL REFERENCES HR.TYPE(TYPE_ID),
   ADDR_LINE1   VARCHAR(60)     NOT NULL,
   ADDR_LINE2   VARCHAR(60)     NULL,
   CITY         VARCHAR(30)     NOT NULL,
   STATE_CD     VARCHAR(3)      NOT NULL,
   POSTAL_CD    VARCHAR(20)     NOT NULL,
   COUNTRY_CD   VARCHAR(3)      NOT NULL
);
COMMENT ON TABLE HR.ADDRESS IS 'Table that contains a Person address data';

CREATE TABLE FINANCE.COSTCENTER(
   COSTCENTER_ID   SERIAL          PRIMARY KEY,
   GL_DEPARTMENT   VARCHAR(10)  NOT NULL,
   GL_LOCATION     VARCHAR(10)  NOT NULL,
   BUSINESS_UNIT   VARCHAR(3)   NOT NULL
);
COMMENT ON TABLE FINANCE.COSTCENTER IS 'Table that contains Finance Cost Center Data';

CREATE TABLE HR.PERSON_COSTCENTER(
   PERSON_ID      INT           NOT NULL REFERENCES HR.PERSON(PERSON_ID),
   COSTCENTER_ID  INT           NOT NULL REFERENCES FINANCE.COSTCENTER(COSTCENTER_ID),
   EFF_DATE       DATE          NOT NULL,
   END_DATE       DATE          NULL
);
COMMENT ON TABLE HR.PERSON_COSTCENTER IS 'Table that contains the history of Person associations with Finance Cost Centers';

